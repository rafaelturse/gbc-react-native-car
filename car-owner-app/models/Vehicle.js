export class Vehicle {
  constructor(
    acceleration,
    doors,
    horsepower,
    images,
    name,
    owner,
    range,
    seats,
    year,
    bookedBy
  ) {
    this.acceleration = acceleration;
    this.doors = doors;
    this.horsepower = horsepower;
    this.images = images;
    this.name = name;
    this.owner = owner;
    this.range = range;
    this.seats = seats;
    this.year = year;
    this.bookedBy = bookedBy;
  }
}
